        <div class="guia-section">
          <div class="guia-section-label">Multimodal</div>
          <h2>Além do texto: imagem, áudio e vídeo</h2>
          <p>Os modelos mais recentes já não são apenas de linguagem — são multimodais. Eles processam e geram texto, imagens, áudio e vídeo em conjunto. Um modelo multimodal pode analisar uma foto de um defeito na linha de produção e redigir automaticamente o relatório de não conformidade. Pode ouvir uma reunião e extrair os itens de ação. Pode ler um contrato escaneado e identificar cláusulas problemáticas.</p>
          <p>GPT-4o, Claude 3 e Gemini já têm capacidades multimodais em produção. A tendência é que texto puro se torne apenas uma das interfaces — não a única.</p>
        </div>

        <div class="divider"></div>

        <div class="guia-section">
          <div class="guia-section-label">Panorama 2025–2027</div>
          <h2>O que esperar nos próximos dois anos</h2>
          <p>Modelos ficando menores e mais baratos sem perder capacidade — o que democratiza o uso local, inclusive em manufatura com restrições de privacidade. Agentes multiagentes (vários LLMs colaborando em paralelo) resolvendo problemas complexos que um único modelo não consegue. Raciocínio formal melhorando — modelos errando menos em matemática, lógica e planejamento. Interface de voz se tornando padrão para operações industriais e campo.</p>
          <p>A janela de vantagem competitiva para quem adota agora é real — mas estreita. Empresas que entenderem os fundamentos e começarem a experimentar em 2025 vão ter 18 a 24 meses de distância das que esperarem a tecnologia "ficar pronta".</p>
        </div>

        <div class="business">
          <div class="business-label">O que fazer agora</div>
          <p>Você terminou o guia com o entendimento que separa quem toma boas decisões sobre IA de quem só ouve o hype. O próximo passo é prático: <strong>identificar um processo</strong> na sua operação que seja repetitivo, baseado em texto e de alto volume — e usá-lo como primeiro caso de uso real.</p>
          <p>Não comece pelo mais complexo. Comece pelo que dói todo dia. A mentoria prática é exatamente sobre isso.</p>
