        <div class="guia-section">
          <div class="guia-section-label">Conectando ao seu negócio</div>
          <h2>O LLM não conhece a sua empresa — ainda</h2>
          <p>Um modelo genérico foi treinado com o conhecimento do mundo, mas não sabe nada sobre os seus processos, produtos, clientes ou histórico. Para resolver isso, existem técnicas que conectam o LLM aos seus dados internos sem precisar retreinar o modelo do zero.</p>
          <p>A mais importante delas é conhecida como RAG — o modelo recebe os documentos relevantes da sua empresa junto com a pergunta, e responde como se fosse um especialista interno. Manuais técnicos, contratos, histórico de pedidos, base de conhecimento: tudo isso pode se tornar contexto acessível ao modelo em tempo real.</p>
          <p>É o padrão que transforma um LLM genérico em uma ferramenta específica para o seu negócio — com muito menos alucinação e respostas rastreáveis até a fonte.</p>
        </div>

        <div class="analogy">
          <div class="analogy-label">Analogia</div>
          <p>É como contratar um consultor brilhante e dar para ele ler todos os documentos internos da empresa antes da reunião. Ele já era capaz — agora tem o contexto certo para ser útil de verdade.</p>
        </div>

        <div class="divider"></div>

        <div class="guia-section">
          <div class="guia-section-label">O próximo nível · Agentes</div>
          <h2>LLMs que agem, não só respondem</h2>
          <p>Um LLM convencional recebe um texto e devolve um texto. Um agente de IA é um LLM que também usa ferramentas: busca informações na internet, lê planilhas, envia e-mails, acessa sistemas, executa código, toma decisões em sequência.</p>
          <p>A diferença é fundamental. Um chatbot responde "o prazo de entrega é 5 dias". Um agente verifica o estoque no ERP, consulta a transportadora, calcula o prazo real e já registra a promessa no CRM — tudo na mesma interação.</p>
          <p>Agentes estão passando de experimento para produção em 2025. As empresas que entenderem como orquestrar essas capacidades vão ter uma vantagem operacional significativa nos próximos 2 a 3 anos.</p>
        </div>

        <div class="guia-cards">
          <div class="guia-card">
