        <div class="guia-section">
          <div class="guia-section-label">Definição</div>
          <h2>Um modelo de linguagem de grande escala</h2>
          <p>LLM significa <em>Large Language Model</em> — em português, Modelo de Linguagem de Grande Escala. É um sistema de inteligência artificial treinado para compreender e gerar texto de forma coerente, contextual e, muitas vezes, surpreendentemente capaz.</p>
          <p>A palavra "grande" não é marketing: os modelos modernos têm dezenas ou centenas de bilhões de parâmetros — os números internos que representam tudo que o modelo aprendeu. Para ter uma referência: o cérebro humano tem cerca de 86 bilhões de neurônios.</p>
          <p>ChatGPT, Claude, Gemini, Llama — todos são LLMs. São a mesma categoria de tecnologia com diferentes treinos, tamanhos e objetivos.</p>
        </div>

        <div class="analogy">
          <div class="analogy-label">Analogia</div>
          <p>Imagine um funcionário que leu todos os livros, artigos, sites e e-mails que existem na internet. Ele não "entende" como um humano — mas aprendeu os padrões da linguagem tão profundamente que consegue responder, redigir, resumir e raciocinar com uma competência impressionante.</p>
        </div>

        <div class="guia-section">
          <div class="guia-section-label">O que ele faz, de fato</div>
          <h2>Previsão de token como superpotência</h2>
          <p>Tecnicamente, um LLM faz uma coisa só: dado um texto de entrada, ele prevê qual é o próximo pedaço de texto mais provável. Faz isso um fragmento de cada vez, em sequência, até completar a resposta.</p>
          <p>Parece simples. Mas para prever bem o próximo fragmento em qualquer contexto — jurídico, médico, técnico, criativo — o modelo precisa ter aprendido uma quantidade absurda de conhecimento sobre o mundo.</p>
        </div>

